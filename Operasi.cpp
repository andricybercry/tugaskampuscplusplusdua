#include <iostream>
using namespace std;
//I.S Program Memasukan data
//F.S Program Menampilkan Operasi Perhitungan

int main() {
	
	int a = 21;
	int b = 10;
	int c ;
	//Algoritma
	//Tugas
	//Pertambahan
	c = a + b;
	cout << c << endl ;

	//Pengurangan
	c = a - b;
	cout << c << endl ;

	//Perkalian
	c = a * b;
	cout << c << endl ;

	//Pembagian
	c = a / b;
	cout << c << endl ;

	//Modulus
	c = a % b;
	cout << c << endl ;

	//Data di tambah 1
	c = a++;
	cout << c << endl ;

	//Data di kurang 1
	c = a--;
	cout << c << endl ;

	return 0;
}